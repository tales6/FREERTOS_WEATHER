{
    uint8_t temp, t, t1;
    uint8_t y0 = y;

    if (c < ' ' || c > '~') return;

    uint8_t *pfont = (uint8_t *)oled_asc2_1608[c - ' '];

    for (t = 0; t < 16; t++)
    {
        temp = pfont[t];
        for (t1 = 0; t1 < 8; t1++)
        {
            if (temp & 0x80)
            {
                oled_draw_point(x, y, 1);
                oled_draw_point(x + 1, y, 1);
                oled_draw_point(x, y + 1, 1);
                oled_draw_point(x + 1, y + 1, 1);
            }
            temp <<= 1;
            y += 2;
            if ((y - y0) == 32)
            {
                y = y0;
                x += 2;
                break;
            }
        }
    }
}

void oled_show_big_string(uint8_t x, uint8_t y, const char *p)
{
    while (*p)
    {
        if (*p < ' ' || *p > '~') { p++; continue; }
        if (x > (128 - 16))
        {
            x = 0;
            y += 32;
        }
        if (y > (64 - 32))
        {
            y = 0;
            x = 0;
        }
        oled_show_big_char(x, y, *p);
        x += 16;
        p++;
    }
}


