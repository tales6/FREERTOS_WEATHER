void oled_show_char(uint8_t x, uint8_t y, char c)
{
    uint8_t temp, t, t1;
    uint8_t y0 = y;
    uint8_t *pfont = 0;
    uint8_t csize = 16;

    if (c < ' ' || c > '~') return;

    uint8_t chr = c - ' ';

    pfont = (uint8_t *)oled_asc2_1608[chr];

    for (t = 0; t < csize; t++)
    {
        temp = pfont[t];

        for (t1 = 0; t1 < 8; t1++)
        {
            if (temp & 0x80) oled_draw_point(x, y, 1);
            else oled_draw_point(x, y, 0);

            temp <<= 1;
            y++;

            if ((y - y0) == 16)
            {
                y = y0;
                x++;
                break;
            }
        }
    }
}

void oled_show_string(uint8_t x, uint8_t y, const char *p)
{
    while ((*p <= '~') && (*p >= ' '))
    {
        if (x > (128 - 8))
        {
            x = 0;
            y += 16;
        }

        if (y > (64 - 16))
        {
            y = 0;
            x = 0;
        }

        oled_show_char(x, y, *p);
        x += 8;
        p++;
    }
}

